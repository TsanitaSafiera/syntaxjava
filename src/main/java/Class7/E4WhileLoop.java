package Class7;

public class E4WhileLoop {
    public static void main(String[] args) {
            // write a loop that prints the numbers from 1 to 5
            // starting point => 1
            // how many is each step => 1
            // ending point => 5

        int counter=1;
        while (counter<=5){
            System.out.println(counter);
            counter++;
        }

    }
}
