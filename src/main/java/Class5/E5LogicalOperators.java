package Class5;

public class E5LogicalOperators {
    public static void main(String[] args) {
       /* NOT ! it flips the boolean values for example if the value is
        true it will change it to false and if it is false it will change
        it to true
       */

        boolean flag=true;
        // ! operator will reverse the the condition
        // and it will get printed

        System.out.println(!flag);
        System.out.println(flag);

        String name="semir";
        // only works with boolean data type
        //System.out.println((!name)); //we will get an error
    }
}
