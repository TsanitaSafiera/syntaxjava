package Class9;

public class E10Arrays {
    public static void main(String[] args) {

        int [] numbers={45,50,20,30,-50,45};
        // after the array type iter and intelij will write an
        // enhanced for loop for you
        for (int number : numbers) {
         /*
         we can place the cursor on the for loop and click on the bulb
         icon to change the loop with another loop
         */

            System.out.println(number);




        }
    }
}
